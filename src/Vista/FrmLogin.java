    private void lecturaCuentas(){
        try{
            File login = new File("src\\login.csv");
            Scanner scLogin = new Scanner(login);
            while(scLogin.hasNextLine()){
                String datosCuenta = scLogin.nextLine();
                Scanner scDatos = new Scanner(datosCuenta);
                scDatos.useDelimiter("\\s*,\\s*");
                String usuario = scDatos.next();
                String password = scDatos.next();
                char desencriptar[] = password.toCharArray();
                for(int i = 0; i < desencriptar.length; i++){
                    if ((int) desencriptar[i] % 2 == 0)
                        desencriptar[i] = (char) (desencriptar[i] + (char) 2);
                    if ((int) desencriptar[i] % 2 == 1)
                        desencriptar[i] = (char) (desencriptar[i] - (char) 2);
                }
                password = String.valueOf(desencriptar);
                Cuenta cuenta = new Cuenta(usuario, password);
                listaCuentas.add(cuenta);
            }
            scLogin.close();
        }catch(FileNotFoundException e1){
            e1.printStackTrace();
        }
    }

    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        int ingresa = -1;
        try{
            String usuarioTxt = this.txtUsuario.getText();
            String passwordTxt = String.valueOf(txtPassword.getPassword());
            if(usuarioTxt.isEmpty()||passwordTxt.isEmpty())
                throw new EntradaInvalidaExcepcion("Rellene todos los campos");
            for (int i = 0; i < listaCuentas.size(); i++) {
                if (usuarioTxt.equals(listaCuentas.get(i).getUsuario()) && passwordTxt.equals(listaCuentas.get(i).getPassword()))
                    ingresa = i;
            }
            if(ingresa == -1)
                JOptionPane.showMessageDialog(rootPane," El usuario o contraseÃ±a es incorrecta");
            else{
                new FrmCalificaciones().setVisible(true);
                this.dispose();
            }
        }catch(EntradaInvalidaExcepcion e2){
            JOptionPane.showMessageDialog(rootPane, e2.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

