    private void btnRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseActionPerformed
        // TODO add your handling code here:
        try{
            String usuarioTxt = this.txtUsuario.getText();
            String passwordTxt = String.valueOf(txtPassword.getPassword());
            if(usuarioTxt.isEmpty()||passwordTxt.isEmpty())
                throw new EntradaInvalidaExcepcion("Rellene todos los campos");
            FileWriter registroCSV = new FileWriter("src\\archivos\\login.csv", true);
            PrintWriter escribir = new PrintWriter(registroCSV);
            char encriptar[] = passwordTxt.toCharArray();
            for(int i = 0; i < encriptar.length; i++) {
                if ((int) encriptar[i] % 2 == 0)
                    encriptar[i] = (char) (encriptar[i] - (char) 2);
                if ((int) encriptar[i] % 2 == 1)
                   encriptar[i] = (char) (encriptar[i] + (char) 2);
            }
            passwordTxt = String.valueOf(encriptar);
            escribir.println(usuarioTxt + "," + passwordTxt);
            JOptionPane.showMessageDialog(rootPane, "La cuenta ha sido registrada con exito");
            registroCSV.close();
        }catch(EntradaInvalidaExcepcion e1){
            JOptionPane.showMessageDialog(rootPane, e1.getMessage());
        }catch(IOException e2){
            JOptionPane.showMessageDialog(rootPane, "Problemas con la creaciÃ³n del archivo de salida");
        }
    }//GEN-LAST:event_btnRegistrarseActionPerformed

